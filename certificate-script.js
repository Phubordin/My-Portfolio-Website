    // ‡πÄ‡∏£‡∏µ‡∏¢‡∏Å applyDarkModeToElements ‡πÇ‡∏î‡∏¢‡∏ï‡∏£‡∏á‡∏´‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å render
    applyDarkModeToElements();
